                <ul>
                    <li><em>Imprensa de Gutenberg:</em> Criada por volta de 1450, permitiu a produção em massa de livros e a rápida difusão de ideias.</li>
                    <li><em>Instrumentos de Navegação:</em> Astrolábio e bússola facilitaram as Grandes Navegações.</li>
                    <li><em>Telescópio e Microscópio:</em> Revolucionaram a astronomia e a biologia no início do século XVII.</li>
                    <li><em>Termômetro e Barômetro:</em> Avanços na meteorologia e no estudo do clima.</li>
                </ul>
            </li>
            <li><strong>Conquistas Científicas e Intelectuais:</strong>
                <ul>
                    <li><em>Teoria Heliocêntrica de Copérnico:</em> Mudou a visão do universo, defendida posteriormente por Galileu.</li>
                    <li><em>Ciência Moderna:</em> Métodos científicos propostos por Bacon e Descartes focaram na observação e no raciocínio lógico.</li>
                    <li><em>Descobertas Geográficas:</em> Explorações de Colombo, Vasco da Gama e Magalhães abriram novas rotas e possibilitaram colonizações.</li>
                </ul>
            </li>
        </ul>
    </section>

