    <section class="social-life-section">
        <h2>Vida Social e Cultural</h2>
        <p><em>Estrutura Social:</em> Dividida entre nobreza, clero e camponeses, com a burguesia emergindo através do comércio e a prática da escravidão expandindo-se nas colônias.</p>
        <p><em>Religião e Reforma:</em> A Reforma Protestante criticou a Igreja Católica e gerou novas vertentes cristãs, enquanto a Contrarreforma reafirmou sua autoridade. Conflitos religiosos, como a Guerra dos Trinta Anos, marcaram o período.</p>
        <p><em>Renascimento Cultural:</em> Artistas como Leonardo da Vinci e escritores como Shakespeare exploraram temas humanos, revolucionando a arte e a literatura.</p>
        <p><em>Iluminismo e Filosofia:</em> Voltaire, Montesquieu e Rousseau defenderam ideias de liberdade e igualdade que inspirariam revoluções e mudanças políticas.</p>
        <p><em>Economia e Comércio:</em> O mercantilismo incentivou a acumulação de riquezas e a exploração de colônias, levando ao desenvolvimento do comércio internacional.</p>
