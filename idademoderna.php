    <section class="periods-section">
        <h2>Períodos da Idade Moderna</h2>
        <ul>
            <li><strong>Renascimento (séculos XV - XVI):</strong> Movimento cultural e intelectual que valorizou a ciência, a arte e a cultura greco-romana, com o humanismo colocando o ser humano no centro das preocupações.</li>
            <li><strong>Grandes Navegações e Expansão Marítima (séculos XV - XVII):</strong> Potências europeias como Portugal e Espanha exploraram novos territórios, expandindo o comércio global.</li>
            <li><strong>Reforma Protestante e Contrarreforma (século XVI):</strong> A Reforma Protestante criticou a Igreja Católica, levando ao surgimento do protestantismo e à reação católica com a Contrarreforma.</li>
            <li><strong>Absolutismo e Iluminismo (séculos XVII - XVIII):</strong> As monarquias absolutistas consolidaram seu poder, enquanto o Iluminismo trouxe ideias de liberdade e igualdade, influenciando revoluções futuras.</li>
        </ul>
    </section>

